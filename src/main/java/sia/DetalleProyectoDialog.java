    private void cargarDatos() {
        Proyecto p = sistema.buscarProyecto(idProyecto);
        if (p != null) {
            txtIdProyecto.setText(p.getIdProyecto());
            txtNombre.setText(p.getNombre());
            txtDescripcion.setText(p.getDescripcion());
            txtFondos.setText(String.valueOf(p.getFondos()));
            txtSubRama.setText(p.getSubRamaCarrera());
        } else {
            txtIdProyecto.setText("No encontrado");
            txtNombre.setText("No encontrado");
            txtDescripcion.setText("No encontrado");
            txtFondos.setText("");
            txtSubRama.setText("");
        }
    }
    

