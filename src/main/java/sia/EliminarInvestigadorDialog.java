    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        String id = txtId.getText().trim();
        String nombreUsuario = txtNombre.getText().trim();

        if (id.isEmpty() || nombreUsuario.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Debes ingresar el ID y tu nombre.", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }
        try {
            boolean ok = sistema.eliminarInvestigador(idProyecto, id);
            if (ok) {
                JOptionPane.showMessageDialog(this, "Investigador eliminado correctamente por: " + nombreUsuario);
                dispose();
            } else {
                JOptionPane.showMessageDialog(this, "No se pudo eliminar el investigador.", "Error", JOptionPane.ERROR_MESSAGE);
            }
        } catch (ProyectoNotFoundException ex) {
            JOptionPane.showMessageDialog(this, "Proyecto no encontrado.", "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnEliminarActionPerformed

