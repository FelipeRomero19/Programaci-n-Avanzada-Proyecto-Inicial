    private void buscarYMostrarNombreInvestigador() {
        String id = txtId.getText().trim();
        if (id.isEmpty()) {
            txtNombre.setText("");
            return;
        }
        String nombre = sistema.buscarNombreInvestigador(idProyecto, id); // Debes tener este m√©todo en tu clase Sistema
        if (nombre != null && !nombre.isEmpty()) {
            txtNombre.setText(nombre);
        } else {
            txtNombre.setText("No encontrado");
            // Opcional: JOptionPane.showMessageDialog(this, "Investigador no encontrado.", "Error", JOptionPane.ERROR_MESSAGE);
        }
    }

