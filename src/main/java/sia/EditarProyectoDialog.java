    private void btnGuardarCambiosActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarCambiosActionPerformed
        if (proyectoActual == null) return;

        String id = proyectoActual.getIdProyecto();
        String nombre = txtNombre.getText().trim();
        String descripcion = txtDescripcion.getText().trim();
        String subrama = txtSubrama.getText().trim();
        String fondosStr = txtFondos.getText().trim();

        if (nombre.isEmpty()) nombre = proyectoActual.getNombre();
        if (descripcion.isEmpty()) descripcion = proyectoActual.getDescripcion();
        if (subrama.isEmpty()) subrama = proyectoActual.getSubRamaCarrera();

        double fondos = proyectoActual.getFondos();
        if (!fondosStr.isEmpty()) {
            try {
                fondos = Double.parseDouble(fondosStr);
                if (fondos < 0) {
                    javax.swing.JOptionPane.showMessageDialog(this, "El monto de fondos no puede ser negativo.", "Advertencia", javax.swing.JOptionPane.WARNING_MESSAGE);
                    txtFondos.requestFocus();
                    return;
                }
            } catch (NumberFormatException ex) {
                javax.swing.JOptionPane.showMessageDialog(this, "Fondos inv√°lidos, se mantiene el anterior.", "Advertencia", javax.swing.JOptionPane.WARNING_MESSAGE);
                txtFondos.setText(Double.toString(proyectoActual.getFondos()));
                txtFondos.requestFocus();
                return;
            }
        }

        try {
            sistema.editarProyecto(id, nombre, descripcion, fondos, subrama);
            javax.swing.JOptionPane.showMessageDialog(this, "Proyecto actualizado correctamente.");
            dispose();
        } catch (sia.exceptions.ProyectoNotFoundException e) {
            javax.swing.JOptionPane.showMessageDialog(this, "Error: " + e.getMessage(), "Error", javax.swing.JOptionPane.ERROR_MESSAGE);
        }

    }//GEN-LAST:event_btnGuardarCambiosActionPerformed

