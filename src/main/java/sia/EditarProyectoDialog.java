    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
        String id = txtId.getText().trim();
        Proyecto p = sistema.buscarProyecto(id);
        if (p == null) {
            javax.swing.JOptionPane.showMessageDialog(this, "Proyecto no encontrado. Por favor, verifica el ID e int√©ntalo nuevamente.", "Error", javax.swing.JOptionPane.ERROR_MESSAGE);
            proyectoActual = null;
            txtNombre.setText("");
            txtDescripcion.setText("");
            txtFondos.setText("");
            txtSubrama.setText("");
            setFieldsEnabled(false); // Deshabilita todo excepto el campo ID y Buscar
            txtId.setEnabled(true); // Por si acaso
            btnBuscar.setEnabled(true);
            return;
        }
        proyectoActual = p;
        txtNombre.setText(p.getNombre());
        txtDescripcion.setText(p.getDescripcion());
        txtFondos.setText(Double.toString(p.getFondos()));
        txtSubrama.setText(p.getSubRamaCarrera());
        setFieldsEnabled(true);
    }//GEN-LAST:event_btnBuscarActionPerformed

