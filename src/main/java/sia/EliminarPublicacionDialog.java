    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        String id = txtId.getText().trim();

        if (id.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Debe ingresar el ID de la publicaci贸n.", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }
        try {
            boolean ok = sistema.eliminarPublicacion(idProyecto, id);
            if (ok) {
                JOptionPane.showMessageDialog(this, "Publicaci贸n eliminada correctamente.");
                dispose();
            } else {
                JOptionPane.showMessageDialog(this, "No se pudo eliminar la publicaci贸n.", "Error", JOptionPane.ERROR_MESSAGE);
            }
        } catch (ProyectoNotFoundException ex) {
            JOptionPane.showMessageDialog(this, "Proyecto no encontrado.", "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnEliminarActionPerformed

